// 函数简介     根据左边界得到右边界
// 参数说明
// 返回参数
// 使用示例
// 备注信息
//------------------------------------------------------------------------------------------------------------------
void Patch_line_Right_Init(int start, int end)
{
	int t;
	int y;
	//自动调整位置
    if (start < end)
    {
        t = start;
        start = end;
        end = t;
    }
    for(y = start; y < end; y++)
    {
        if(y >= 120) continue;
        if(Left_Line[y] < 20 && Right_Line[y] < MT9V03X_W-20)
		{
			Right_Line[y] = Left_Line[y] + Ring_Width_Table[y];
		}
        // 左线保持不变
        // 右线 = 左线 + 当前行标准宽度
//        Right_Line[y] = Left_Line[y] + Ring_Width_Table[y];
    }
}

