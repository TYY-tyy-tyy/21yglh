// 函数简介     根据左边界得到右边界
// 参数说明
// 返回参数
// 使用示例
// 备注信息
//------------------------------------------------------------------------------------------------------------------
void Right_Patch_Init(void)
{
	int y;
    for(y = 0; y < MT9V03X_H; y++)
    {
        if(y >= 120) continue;
        
        // 左线保持不变
        // 右线 = 左线 + 当前行标准宽度
        Right_Line[y] = Left_Line[y] + Ring_Width_Table[y];
    }
}

